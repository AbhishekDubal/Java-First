class Program
{
    public static void main(String args[])
    {
       //Assignment Operator(=, +=,-=,*=,/=,%=)#It is unary operator. Because, it will work on single operand itself.
       int x;

       x=10;//simple(=)

       x+=5;//compound
       System.out.println(x);

       x-=2;
       System.out.println(x);
    }
}