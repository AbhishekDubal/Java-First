
class Program
{
    public static void main(String args[])
    {
       //Looping Statements (If you want to repeat certain line of code repeatedly)
       //#4 Nested for-loop
      
       for(int i = 1; i<=10; i++){
            for(int j = 1; j<=10; j++){
                System.out.print("*");
            }
            System.out.println();
       }
    }
}